mod bounded;
mod dictionary;

use base64::Engine;
use base64::engine::general_purpose::URL_SAFE_NO_PAD;
pub use dictionary::DICTIONARY_V1;
use flate2::{Compress, Compression, FlushCompress};

pub const MAX_DECODED_SIZE: usize = 64 * 1024;

const FRAME_TAG: u8 = 0x00;
const SCHEME_RAW: u8 = 0x01;
const SCHEME_DICT: u8 = 0x02;

#[derive(Debug, PartialEq, Eq)]
pub enum DecodeError {
    InvalidBase64,
    UnsupportedScheme,
    Corrupt,
    TooLarge,
}

/// Decodes a `/r/<payload>` path segment back into the HTML bytes it
/// encodes. Payloads that don't start with the `0x00` frame tag after
/// base64url-decoding are treated as legacy, uncompressed payloads (the
/// format every badge used before compression support existed) -- that
/// path must keep working forever, since already-deployed badge URLs rely
/// on it with no framing at all.
pub fn decode(payload: &str) -> Result<Vec<u8>, DecodeError> {
    let decoded = URL_SAFE_NO_PAD
        .decode(payload)
        .map_err(|_| DecodeError::InvalidBase64)?;

    let bytes = match decoded.first() {
        Some(&FRAME_TAG) => {
            let scheme = *decoded.get(1).ok_or(DecodeError::Corrupt)?;
            let body = &decoded[2..];
            let dictionary = match scheme {
                SCHEME_RAW => None,
                SCHEME_DICT => Some(DICTIONARY_V1),
                _ => return Err(DecodeError::UnsupportedScheme),
            };
            bounded::inflate_bounded(body, dictionary).map_err(|e| match e {
                bounded::InflateError::Corrupt => DecodeError::Corrupt,
                bounded::InflateError::TooLarge => DecodeError::TooLarge,
            })?
        }
        _ => decoded,
    };

    if bytes.len() > MAX_DECODED_SIZE {
        return Err(DecodeError::TooLarge);
    }

    Ok(bytes)
}

fn deflate(html: &[u8], dictionary: Option<&[u8]>) -> Vec<u8> {
    let mut compress = Compress::new(Compression::best(), false);
    if let Some(dictionary) = dictionary {
        compress
            .set_dictionary(dictionary)
            .expect("preset dictionary must be valid");
    }

    let mut out = Vec::with_capacity(html.len());
    loop {
        let before_in = compress.total_in() as usize;
        let before_out = compress.total_out() as usize;
        out.resize(before_out + 4096, 0);
        let status = compress
            .compress(&html[before_in..], &mut out[before_out..], FlushCompress::Finish)
            .expect("in-memory deflate cannot fail");
        out.truncate(compress.total_out() as usize);
        if status == flate2::Status::StreamEnd {
            break;
        }
    }
    out
}

/// Encodes HTML bytes into a `/r/<payload>` path segment. Tries every
/// compression scheme plus the legacy uncompressed form and keeps whichever
/// produces the strictly shortest base64url string, so compression can
/// never make a URL longer than the legacy encoding would have.
pub fn encode(html: &[u8]) -> String {
    let mut best = URL_SAFE_NO_PAD.encode(html);

    let candidates = [
        (SCHEME_RAW, None),
        (SCHEME_DICT, Some(DICTIONARY_V1)),
    ];
    for (scheme, dictionary) in candidates {
        let compressed = deflate(html, dictionary);
        let mut framed = Vec::with_capacity(compressed.len() + 2);
        framed.push(FRAME_TAG);
        framed.push(scheme);
        framed.extend_from_slice(&compressed);

        let candidate = URL_SAFE_NO_PAD.encode(&framed);
        if candidate.len() < best.len() {
            best = candidate;
        }
    }

    best
}

