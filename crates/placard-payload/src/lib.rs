    #[test]
    fn rejects_unknown_scheme() {
        let payload = URL_SAFE_NO_PAD.encode([FRAME_TAG, 0xFF]);
        assert_eq!(decode(&payload), Err(DecodeError::UnsupportedScheme));
    }

    #[test]
    fn rejects_corrupt_deflate_stream() {
        let payload = URL_SAFE_NO_PAD.encode([FRAME_TAG, SCHEME_RAW, 0xFF, 0xFF, 0xFF]);
        assert_eq!(decode(&payload), Err(DecodeError::Corrupt));
    }
