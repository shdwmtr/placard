    #[test]
    fn dictionary_scheme_wins_on_corpus_like_content() {
        let html = PLACARD_HTML.as_bytes();
        let no_dict = {
            let compressed = deflate(html, None);
            let mut framed = vec![FRAME_TAG, SCHEME_RAW];
            framed.extend_from_slice(&compressed);
            URL_SAFE_NO_PAD.encode(&framed)
        };
        let with_dict = {
            let compressed = deflate(html, Some(DICTIONARY_V1));
            let mut framed = vec![FRAME_TAG, SCHEME_DICT];
            framed.extend_from_slice(&compressed);
            URL_SAFE_NO_PAD.encode(&framed)
        };

        assert!(with_dict.len() < no_dict.len());
        assert_eq!(encode(html), with_dict);
    }

    #[test]
    fn incompressible_content_falls_back_to_legacy() {
        // A pseudo-random byte sequence has no internal repetition and
        // shares nothing with the dictionary, so DEFLATE can only ever make
        // it larger once base64 + framing overhead is added back on top.
        let mut bytes = Vec::with_capacity(4096);
        let mut state: u32 = 0x2545F491;
        for _ in 0..4096 {
            state ^= state << 13;
            state ^= state >> 17;
            state ^= state << 5;
            bytes.push((state & 0xff) as u8);
        }

        let legacy = URL_SAFE_NO_PAD.encode(&bytes);
        assert_eq!(encode(&bytes), legacy);
    }

